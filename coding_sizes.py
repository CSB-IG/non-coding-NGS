lengths = {}
for g in gmap:
    (chrom, start, end) = g.strip().split()
    if chrom in lengths:
        lengths[chrom] += (int(end) - int(start))
    else:
        lengths[chrom] = (int(end) - int(start))


import pprint
pprint.pprint(lengths)
    


